excerpt="""At times, jQuery can induce some scope issues when it comes to using the JavaScript array prototypes. One example in particular is the unshift() array prototype. In the same context as array.push(), Firebug may throw the error that <strong>unshift is not a function</strong>. Honestly, I can't account for why this happens, but in the event you need to use unshift for a jQuery object, I have created a jQuery plugin called <a href="http://www.vertstudios.com/blog/frontpush-jquery-plugin/">.frontpush()</a> that works around the issue.""" 

