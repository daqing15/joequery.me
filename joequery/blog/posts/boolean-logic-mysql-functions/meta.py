excerpt="""Boolean Logic is an essential part of php when dealing with MySQL. If you do not understand boolean logic, a lot of the mysql functions may not make sense to you.

Take the commonly used <a href="http://php.net/manual/en/function.mysql-fetch-array.php">mysql_fetch_array</a> function. It is commonly used with a while loop in this manner:

<pre class="prettyprint">
while($row = mysql_fetch_array($query))
{
//Do something with the record data
}
</pre>

The condition for that while loop might not make sense if you haven't created your own boolean functions.""" 

